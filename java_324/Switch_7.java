public class Switch_7 
{
	public static void main(String[] args) throws Exception
	{
		int ch;

		System.out.print("L.O.V.E  중 하나의 문자를 입력하세요 !");
		ch = System.in.read();

		switch (ch)
		{
		case 76:
			System.out.println("L");
		case 79:
			System.out.println("O");
		case 'V':
			System.out.println("V");
		case 'E':
			System.out.println("E");
		
		}
	}
}

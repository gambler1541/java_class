public class Arithmetic_5 
{
	public static void main(String[] args) 
	{
		int sum = 15, sub = 30, mul = 6, div = 56;

		sum += 10; // sum = sum + 10
		sub -= 6; // sub = sub - 6
		mul *= 4; // mul = mul * 4
		div /= 7; // div = div / 7

		System.out.println("sum: " + sum);
		System.out.println("sub: " + sub);
		System.out.println("mul: " + mul);
		System.out.println("div: " + div);
	}
}
